let plants = ['broccoli', 'cauliflower', 'cabbage', 'kale', 'tomato'];

plants.push("cucumber")
// Push method adds element to array
console.log(plants)

// Pop method gets the last value of array
let newPlants = plants.pop()
console.log(newPlants)

